/*
 Write a program to work with literals.
 */
class literal {
    public static void main(String[] args) {
        double length = 23.89;
        double width = 12.45;
        double area = length*width;
        System.out.println("Area of the Rectangle is "+area);
    }
}
/*
OUTPUT
 Area of the Rectangle is 297.4305
 */