// Write a program to work with While loop

public class While_loop {
    public static void main(String[] args) {
        System.out.println("2's Table - ");
        int i = 1;
        while ( i <= 10) {
            System.out.println("2 * "+ i +" = "+ (2*i));
            i++;
        }
    }
}

